

<h2>Overview Of The Affiliate Program</h2>

<ul><li>Commissions are paid in either Adcoin/advertising credits or Bitcoin (your preference) </li>
<li>Commissions are residual and continuous (paid each time the buyer pays)</li>
<li>You can increase your earnings by recruiting publishers and other affiliates</li>
<li>You earn commissions only on the sales of your recruited publishers and affiliates and not for recruiting them</li>
<li>You can register advertisers, publishers and other affiliates all for free (no registration or membership fees)</li>
<li>The motivation for website owners to pay is so they can get better ad positions.</li>
<li>Advertising credits (aka Adcoins) can be purchased for Bitcoin on a one for one basis</li>
<li>Affiliates and Publishers can sell advertising credits (aka Adcoins) they have earned to their own customers as well as other Bungeebones customers </li>
<li>Advertising credits (aka Adcoins) can be earned as an affiliate or a publisher</li>
<li>Links are displayed twenty per page</li>
<li>Fees start at less than pennies per day</li>
<li>Flat fee per day, no per click charges</li>
<li>Fees established by competitive bidding</li>
</ul>

